    private void showMyCoursesButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_showMyCoursesButtonActionPerformed
        // TODO add your handling code here:
        ArrayList<Course> courses;
        try {
            courses = Service.getEnrolledCoursesByUserId(user.getId());
            searchResultLabel.setText("");
            for (Course course : courses) {
                searchResultLabel.setText(searchResultLabel.getText() + course.getName() + "\n");
            }
        } catch (BusinessException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }//GEN-LAST:event_showMyCoursesButtonActionPerformed

