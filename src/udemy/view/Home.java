    public Home(int mode, User user) {
        this.user = user;
        JPanel studentOrTeacherPanel = null;
        if (mode == UsersTypesEnum.STUDENT.getCode()) {
            studentOrTeacherPanel = new Student(user);
        } else if (mode == UsersTypesEnum.TEACHER.getCode()) {
            studentOrTeacherPanel = new Teacher(user);
        }

        studentOrTeacherPanel.setBounds(0, 120, 450, 100);
        getContentPane().add(studentOrTeacherPanel);
        initComponents();
    }

